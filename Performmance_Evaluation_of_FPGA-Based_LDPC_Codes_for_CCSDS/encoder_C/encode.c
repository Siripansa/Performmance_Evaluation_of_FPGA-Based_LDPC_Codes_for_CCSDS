/*Function to left Rotate arr[] of
  size n by 1*/
void leftRotatebyOne(in arr[], int n)
{
    int temp ;
    int i;
    temp = arr[n-1];
    for (i = n-1; i > 0; i--)
    {
        arr[i] = arr[i-1];
    }
    arr[0] = temp;
}

/*Function to left rotate arr[] of size n by d*/
void leftRotate(in arr[], int d, int n)
{
    for (int i = 0; i < d; i++)
    {
        leftRotatebyOne(arr, n);
    }
}

